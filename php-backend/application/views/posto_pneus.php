							<h2>POSTO PNEUS</h2>
						</header>
						<section class="wrapper style5">
							<div class="inner">
                                <p>No centro de São Pedro do Sul, encontra uma área renovada com 2000 m2, a pensar em si e no seu automóvel ligeiro, pesado ou industrial.</p>

                                <p>Equipamento moderno, dois postos de alinhamento 3D (Hunter e Sun), marcas lideres nos equipamentos e know-how de alinhamento, com as mais recentes tecnologias em equilibragem e montagem, pessoal qualificado e possibilidade de escolha entre mais de 5000 referências em pneus ligeiros, comerciais, agrícolas, industriais e pesados, com promoções mensais, onde procuramos ter sempre o que a sua viatura necessita, aos melhores preços.</p>

                                <p>Fruto da parceria com a Michelin, pertencemos à rede de centros especialistas do pneu – VIALIDER – que se traduz num compromisso de profissionalismo e melhoria contínua no atendimento ao cliente, certificado por uma auditoria anual e manual de procedimentos. Paralelamente, está a decorrer o processo de alargamento da certificação pela ISO 9001:2008, no âmbito dos pneus e serviços a pneus.</p>

                                <p>Simultaneamente somos também representantes e parceiros de negócio dos outros dois grupos líderes de mercado - Bridgestone/Firestone e Continental.</p>

                                <p>Nos nossos postos poderá ainda encontrar uma ampla oferta de serviços de manutenção rápida auto, nomeadamente:</p>
                                <ul>
                                    <li>Estação de serviço para mudanças de óleo e filtros, lavagens simples, onde se procede à recolha, armazenagem e encaminhamento de todos os resíduos.</li>

                                    <li>Substituição de baterias, escovas limpa vidros, jantes, calços e discos de travões, foles, ponteiras de direcção, focagem de faróis.</li>
                                </ul>

                                <p>Na área específica dos pneus pesados, a Silva & Carvalhas está equipada com as mais recentes e seguras tecnologias de alinhamento em fossa técnica com elevação pneumática, equilibragem e montagem no posto.</p>

                                <p>A capacidade de stock mínimo que possuímos em pneus pesados - nunca inferior a 2500 unidades nos grupos Bridgestone/Firestone, Continental, Michelin e Goodyear – permite-nos ter uma grande e rápida capacidade perante as necessidades dos clientes/frotas</p>
                                
                                <p>Com duas unidades móveis de serviço a frotas e assistência em estrada, prolongamos a nossa capacidade de resposta ao cliente na área dos pesados, agrícolas, florestais e industriais.</p>

                                <p>Neste contexto a empresa é parceira do serviço Michelin Euro Assist e Bridgestone/Firestone Truck Point.</p>
                                
