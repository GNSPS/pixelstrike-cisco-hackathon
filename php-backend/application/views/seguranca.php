                                <p>Para uma maior durabilidade dos pneus:</p>
                                
                                <ul>
                                    <li>Consuza sempre com a pressão indicada pelo fabricante e verifique a pressão pelo menos uma vez por mês;</li>
                                    <li>Evite cargas desnecessárias;</li>
                                    <li>Faça uma condução suave;</li>
