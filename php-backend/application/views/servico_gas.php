                                <p>No centro de São Pedro do Sul, encontra uma área com 2000 m2, a pensar em si e no seu automóvel.</p>

                                <p>Equipamento moderno, dois postos de alinhamento 3D (Hunter e Sun), marcas líderes nos equipamentos e know-how de alinhamento, com as mais recentes tecnologias em equilibragem e montagem. Pessoal qualificado e possibilidade de escolha entre mais de 5000 referências em pneus, com promoções mensais, onde procuramos ter sempre o que a sua viatura necessita, aos melhores preços.</p>

                                <p>Somos representantes e parceiros de negócio dos principais grupos líderes de mercado – Bridgestone/Firestone, Continental, Goodyear e Michelin (Centro de Qualidade Michelin – que se traduz num compromisso de profissionalismo e melhoria contínua no atendimento ao cliente, certificado por auditorias periódicas).</p>
                                
                                <p><b>Manutenção/Revisão rápida auto</b>, nomeadamente:</p>
                                
                                <ul>
                                    <li>Substituição de fluídos e filtros: lubrificantes e filtros óleo, ar, habitáculo e combustível</li>
                                    <li>Travagem: pastilhas e discos de travões</li>
                                    <li>Alinhamento de direção, ponteiras, terminais e foles, equilibragem de pneus</li>
                                    <li>Baterias, lâmpadas auto, focagem de faróis</li>
                                    <li>Carregamento de ar condicionado</li>
                                    <li>Lavagens simples, escovas limpa vidros</li>
                                    <li>Reparação de furos</li> 
                                    <li>Revisão programada</li>
                                </ul>
                                
                                <p>Todos os resíduos provenientes das nossas oficinas são armazenados e encaminhados para os organismos competentes.</p>
                                
                                <p>Para informações de contactos, horários, entregas ao domicílio e emergências por favor refira-se à <a href="http://www.vulcano.pt">página dos Contactos</a></p>
                                
